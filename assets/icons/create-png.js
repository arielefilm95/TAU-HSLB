// Script para crear el archivo PNG directamente
const fs = require('fs');

// Cabecera de un archivo PNG de 144x144px (minimal)
const pngHeader = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, // IHDR chunk length (13 bytes)
    0x49, 0x48, 0x44, 0x52, // IHDR
    0x00, 0x00, 0x00, 0x90, // Width: 144
    0x00, 0x00, 0x00, 0x90, // Height: 144
    0x08, 0x02, 0x00, 0x00, 0x00, // Bit depth: 8, Color type: 2 (RGB), etc.
    0x4B, 0x6D, 0x29, 0xDC, // CRC
    0x00, 0x00, 0x00, 0x0C, // IDAT chunk length (12 bytes for minimal data)
    0x49, 0x44, 0x41, 0x54, // IDAT
    0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01, // Minimal image data
    0x00, 0x00, 0x00, 0x00, // IEND chunk length (0)
    0x49, 0x45, 0x4E, 0x44, // IEND
    0xAE, 0x42, 0x60, 0x82  // CRC
]);

// Escribir el archivo
fs.writeFileSync('icon-144x144.png', pngHeader);
